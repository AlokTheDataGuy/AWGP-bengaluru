/**
 * Generates lib/photoManifest.js by scanning fixed public/assets/<…> photo
 * folders used by server components (programs/[slug], activities/gau-seva).
 *
 * Why: those pages used to call fs.readdirSync(public/assets/…) at build time.
 * Next's output file-tracer then bundled the ENTIRE public/ tree (~430MB) into
 * the serverless functions, blowing past Vercel's 250MB limit. Reading from a
 * static import instead keeps the functions tiny and tree-shakeable — the same
 * approach as gen-gallery-manifest.mjs.
 *
 * Drop images into the folders below and re-run (also runs automatically on
 * `npm run build` via the prebuild hook):
 *
 *   node scripts/gen-photo-manifest.mjs
 */
import { readdirSync, writeFileSync } from 'node:fs';
import { join, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = join(__dirname, '..');
const ASSETS_DIR = join(ROOT, 'public', 'assets');
const OUT_FILE = join(ROOT, 'lib', 'photoManifest.js');

const IMG_RE = /\.(jpe?g|png|webp|avif)$/i;

/**
 * Each entry: a logical key (matches getPhotos(...segments).join('/')), the
 * path segments under public/assets, and any filenames to skip.
 */
const FOLDERS = [
  { key: 'book-exhibitions', segments: ['book-exhibitions'] },
  { key: 'highlights/tree-plantation-drive-2026', segments: ['highlights', 'tree-plantation-drive-2026'] },
  { key: 'tree-plantation/prakriti-mitra-abhiyan', segments: ['tree-plantation', 'prakriti-mitra-abhiyan'] },
  { key: 'gau-seva', segments: ['gau-seva'], exclude: ['table.png'] },
  { key: 'yagya', segments: ['yagya'] },
  { key: 'yagya/deepyagya', segments: ['yagya', 'deepyagya'] },
];

const manifest = {};
let total = 0;

for (const { key, segments, exclude = [] } of FOLDERS) {
  const dir = join(ASSETS_DIR, ...segments);
  let files = [];
  try {
    files = readdirSync(dir)
      .filter((f) => IMG_RE.test(f) && !exclude.includes(f))
      .sort();
  } catch {
    files = [];
  }
  manifest[key] = files.map((f) => `/assets/${segments.join('/')}/${f}`);
  total += files.length;
}

const banner = `// AUTO-GENERATED by scripts/gen-photo-manifest.mjs — do not edit by hand.
// Re-run: node scripts/gen-photo-manifest.mjs  (also runs on \`npm run build\`)
// ${total} photos across ${Object.keys(manifest).length} folders.
`;

const body = `export const PHOTO_MANIFEST = ${JSON.stringify(manifest, null, 2)};
`;

writeFileSync(OUT_FILE, banner + '\n' + body, 'utf8');

console.log(`Wrote ${OUT_FILE}`);
for (const key of Object.keys(manifest)) console.log(`  ${key}: ${manifest[key].length}`);
console.log(`  total: ${total}`);

/**
 * Generates lib/galleryManifest.js by scanning public/assets/gallery/<year>/.
 *
 * The timeline gallery is photo-driven: drop images into
 * public/assets/gallery/2027/ (etc.) and re-run:
 *
 *   node scripts/gen-gallery-manifest.mjs
 *
 * No hand-editing of 500+ filenames. Output is a plain ESM module so it
 * tree-shakes and bundles like any other import.
 */
import { readdirSync, statSync, writeFileSync } from 'node:fs';
import { join, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = join(__dirname, '..');
const GALLERY_DIR = join(ROOT, 'public', 'assets', 'gallery');
const OUT_FILE = join(ROOT, 'lib', 'galleryManifest.js');

const IMG_RE = /\.(jpe?g|png|webp|avif|gif)$/i;
// Files renamed to a plain number (e.g. 1.jpg, 12.jpg) are the curated order:
// they sort first, by numeric value. Everything else follows in natural order.
const NUM_RE = /^(\d+)\.(?:jpe?g|png|webp|avif|gif)$/i;

function isYearDir(name) {
  return /^\d{4}$/.test(name);
}

function sortGallery(a, b) {
  const na = a.match(NUM_RE);
  const nb = b.match(NUM_RE);
  if (na && nb) return Number(na[1]) - Number(nb[1]); // both numbered → numeric order
  if (na) return -1;                                  // numbered files come first
  if (nb) return 1;
  return a.localeCompare(b, undefined, { numeric: true, sensitivity: 'base' });
}

const years = readdirSync(GALLERY_DIR)
  .filter((name) => {
    try {
      return isYearDir(name) && statSync(join(GALLERY_DIR, name)).isDirectory();
    } catch {
      return false;
    }
  })
  .sort();

const manifest = {};
let total = 0;

for (const year of years) {
  const dir = join(GALLERY_DIR, year);
  const files = readdirSync(dir)
    .filter((f) => IMG_RE.test(f))
    .sort(sortGallery);
  manifest[year] = files.map((f) => `/assets/gallery/${year}/${f}`);
  total += files.length;
}

const banner = `// AUTO-GENERATED by scripts/gen-gallery-manifest.mjs — do not edit by hand.
// Re-run: node scripts/gen-gallery-manifest.mjs
// Years: ${years.join(', ')} · ${total} photos total.
`;

const body = `export const GALLERY_YEARS = ${JSON.stringify(years)};

export const GALLERY_MANIFEST = ${JSON.stringify(manifest, null, 2)};

export const GALLERY_TOTAL = ${total};
`;

writeFileSync(OUT_FILE, banner + '\n' + body, 'utf8');

console.log(`Wrote ${OUT_FILE}`);
for (const year of years) console.log(`  ${year}: ${manifest[year].length}`);
console.log(`  total: ${total}`);
